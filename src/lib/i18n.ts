type MessageKey = keyof typeof messages.en;

export function t(
  language: LanguageMode,
  key: MessageKey,
  params: Record<string, string | number> = {}
): string {
  let message: string = messages[language][key] ?? messages.en[key];
  for (const [name, value] of Object.entries(params)) {
    message = message.replaceAll(`{${name}}`, String(value));
  }
  return message;
}

export function mealTypeLabel(language: LanguageMode, type: MealType): string {
  return t(language, `mealType.${type}` as MessageKey);
}

export function languageLabel(language: LanguageMode): string {
  return language.toUpperCase();
}
